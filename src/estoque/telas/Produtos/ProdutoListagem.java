    private void removerMecanicoToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerMecanicoToggleButtonActionPerformed
        // TODO add your handling code here:
        try {
            ControladorProduto cp = new ControladorProduto();
            //Seleciona a linha da coluna.
            int index = tabelaProduto.getSelectedRow();

            if (index >= 0) {
                Produto p = this.listaProdutosGlobal.get(index);

                try {
                    cp.remover(p);
                    JOptionPane.showMessageDialog(rootPane, "Produto Removido com "
                            + "Sucesso");
                    listarProduto();
                } catch (Exception ex) {
                    System.out.println(ex.getMessage());
                    JOptionPane.showMessageDialog(rootPane, "Falha ao remover o"
                            + " Produto");
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Selecione um Produto para"
                        + " Remover");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }

    }//GEN-LAST:event_removerMecanicoToggleButtonActionPerformed

    private void alterarMecanicoToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarMecanicoToggleButtonActionPerformed
        // TODO add your handling code here:
        int index = tabelaProduto.getSelectedRow();

        if (index >= 0) {
            Produto p = this.listaProdutosGlobal.get(index);
            AlterarProdutoForm apf = new AlterarProdutoForm(p, this);
            apf.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Produto para"
                    + " Alterar");
        }
    }//GEN-LAST:event_alterarMecanicoToggleButtonActionPerformed

    private void filtroProdutoTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_filtroProdutoTextFieldKeyPressed
        // TODO add your handling code here:

        DefaultTableModel modelo = new DefaultTableModel();
        //atribuindo as colunas da tabela
        modelo.setColumnIdentifiers(new String[]{"Referência", "Fornecedor",
            "Preço Venda", "Estoque Atual", "Aplicação"});

        if (filtroProdutoTextField.getText().length() > 0) {
            for (Produto p : listaProdutosGlobal) {
                if (p.getReferencia().toLowerCase().contains(
                        filtroProdutoTextField.getText().toLowerCase())) {
                    modelo.addRow(new String[]{"" + p.getReferencia(), 
                        p.getFornecedor().getNomeFantasia(),
                        "R$: " + p.getPrecoVenda(), "" + p.getEstoqueAtual(), p.getAplicacao()});
                }
            }
            tabelaProduto.setModel(modelo);
        } else {
            listarProduto();
        }
    }//GEN-LAST:event_filtroProdutoTextFieldKeyPressed

