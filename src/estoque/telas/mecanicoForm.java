    private void cadastrarMecanicoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarMecanicoButtonActionPerformed
        // TODO add your handling code here:
        
        Mecanico m = new Mecanico();
        
        m.setNome(nomeMecanicojTextField.getText());
        m.setCpf(cpfMecanicoFormattedTextField.getText());
        
        ControladorMecanico cm = new ControladorMecanico();
        try {
            cm.cadastrar(m);
            JOptionPane.showMessageDialog(rootPane, "Mecânico Cadastrado com sucesso");
            limparCamposMecanico();
        } catch (Exception ex) {
          JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
        
        
    }//GEN-LAST:event_cadastrarMecanicoButtonActionPerformed

