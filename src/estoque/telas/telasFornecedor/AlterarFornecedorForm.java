    private void buscarCep(String cep) {

        String cepTratado = cep.replace("-", "");
        String url = "http://viacep.com.br/ws/" + cepTratado + "/json/";

        Util e = new Util();
        try {
            JSONObject obj = new JSONObject(e.getHttpGET(url));
            logradouroAlterarPJTextField.setText(obj.getString("logradouro"));
            estadoAlterarPJTextField.setText(obj.getString("uf"));
            cidadeAlterarPJTextField.setText(obj.getString("localidade"));
            bairroAlterarPJTextField.setText((obj.getString("bairro")));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Cep inválido, informe CEP com 8 dígitos");
            System.out.println(ex.getMessage());
        }
    }

    private void carregarCampos(Fornecedor f) {
        try {
            this.oldCnpj = f.getCnpj();
            nomeFantasiaAlterarPJTextField.setText(f.getNomeFantasia());
            cnpjAlterarPJTextField.setText(f.getCnpj());
            razaoSocialAlterarPJFormattedTextField.setText(f.getRazaoSocial());
            emailAlterarPJFormattedTextField.setText(f.getEmail());
            telefonePrincAlterarPJFormattedTextField.setText(f.getTelefonePrincipal());
            telefoneOpcionalAlterarPJFormattedTextField.setText(f.getTelefoneOpcional());
            cepAlterarPJFormattedTextField.setText(f.getCep());
            buscarCep(f.getCep());
            responsaveisTextField.setText(f.getNomeResponsaveis());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

