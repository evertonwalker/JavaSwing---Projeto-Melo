    private void alterarPJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarPJButtonActionPerformed
        // TODO add your handling code here:
        try {
            ControladorFornecedor cf = new ControladorFornecedor();
            Fornecedor f = new Fornecedor();

            f.setNomeFantasia(nomeFantasiaAlterarPJTextField.getText());
            f.setCnpj(cnpjAlterarPJTextField.getText());
            f.setRazaoSocial(razaoSocialAlterarPJFormattedTextField.getText());
            f.setEmail(emailAlterarPJFormattedTextField.getText());
            f.setTelefonePrincipal(telefonePrincAlterarPJFormattedTextField.getText());
            f.setTelefoneOpcional(telefoneOpcionalAlterarPJFormattedTextField.getText());
            f.setCep(cepAlterarPJFormattedTextField.getText());
            f.setLogradouro(logradouroAlterarPJTextField.getText());
            f.setCidade(cidadeAlterarPJTextField.getText());
            f.setBairro(bairroAlterarPJTextField.getText());
            f.setEstado(estadoAlterarPJTextField.getText());
            f.setNomeResponsaveis(responsaveisTextField.getText());

            cf.atualizar(f, this.oldCnpj);
            JOptionPane.showMessageDialog(rootPane, "Fornecedor Atualizado com"
                    + " Sucesso");
            this.formPai.listarFornecedor();
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_alterarPJButtonActionPerformed

