    private void alterarMecanicoToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarMecanicoToggleButtonActionPerformed
        // TODO add your handling code here:
        
        try {
            ControladorMecanico cmec = new ControladorMecanico();

            Mecanico mec = new Mecanico();

            mec.setCpf(cpfMecanicoAlterarFormattedTextField.getText());
            mec.setNome(nomeMecanicoAlterarTextField.getText());

            cmec.atualizar(mec, this.oldCpf);
            JOptionPane.showMessageDialog(rootPane, "Mec칙nico Atualizado com"
                    + " Sucesso");
            this.formPai.listarMecanico();
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_alterarMecanicoToggleButtonActionPerformed

