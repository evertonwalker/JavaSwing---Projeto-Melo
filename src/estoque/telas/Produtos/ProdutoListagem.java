    public void listarProduto() {

        DefaultTableModel modelo = new DefaultTableModel();
        //atribuindo as colunas da tabela
        modelo.setColumnIdentifiers(new String[]{"Referência", "Fornecedor",
            "Preço Venda", "Estoque Atual", "Aplicação"});
        try {
            ControladorProduto cp = new ControladorProduto();
            this.listaProdutosGlobal = cp.listar();
            for (Produto p : listaProdutosGlobal) {
                modelo.addRow(new String[]{"" + p.getReferencia(), p.getFornecedor().getNomeFantasia(),
                    "R$: " + p.getPrecoVenda(), "" + p.getEstoqueAtual(), p.getAplicacao()});
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
        tabelaProduto.setModel(modelo);

    }

